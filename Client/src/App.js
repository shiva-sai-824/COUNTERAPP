import React, { useState, useEffect, useContext, useReducer, useCallback } from 'react';
import { BrowserRouter as Router, Routes, Route, Link, useNavigate } from 'react-router-dom';
import axios from 'axios';

// Counter context
const CounterContext = React.createContext();

// Reducer function for managing counter state
const counterReducer = (state, action) => {
  switch (action.type) {
    case 'SET':
      return { count: action.count };
    case 'INCREMENT':
      return { count: state.count + 1 };
    case 'DECREMENT':
      return { count: state.count - 1 };
    default:
      return state;
  }
};

const Home = () => {
  const { state } = useContext(CounterContext);
  const [counterData, setCounterData] = useState({count:0,myCount:0});

  const data =async () => {
    try {
      const response = await axios.get('http://localhost:5000/api/counter');
      if(response.data!=null){
        setCounterData(response.data);
      }
      
    } catch (err) {
      console.error(err);
    }
  };

  useEffect(() => {
    data();
  }, []);

  return (
    <div>
      
      <h1>Counter Value: {counterData.count}</h1>
      <h1>my Counter Value: {counterData.myCount}</h1>
      <Link to="/counter">Counter</Link>
    </div>
  );
};

const Counter = () => {
  const { state, dispatch } = useContext(CounterContext);
  const navigate = useNavigate();

  const fetchCounter = useCallback(async () => {
    try {
      const response = await axios.get('http://localhost:5000/api/counter');
      dispatch({ type: 'SET', count: response.data.count });
      console.log(response.data.count);
    } catch (err) {
      console.error(err);
    }
  }, [dispatch]);

  useEffect(() => {
    fetchCounter();
  }, [fetchCounter]);

  const incrementCounter = useCallback(async () => {
    try {
      await axios.post('http://localhost:5000/api/counter/increment', {
        data:"countinc"
      });
      dispatch({ type: 'INCREMENT' });
    } catch (err) {
      console.error(err);
    }
  }, [dispatch]);

  const decrementCounter = useCallback(async () => {
    try {
      await axios.post('http://localhost:5000/api/counter/decrement',{
        data:"countdec"
      });
      dispatch({ type: 'DECREMENT' });
    } catch (err) {
      console.error(err);
    }
  }, [dispatch]);

  return (
    <div>
      <h2>Counter</h2>
      <p>Count: {state.count}</p>
      <button onClick={incrementCounter}>Increment</button>
      <button onClick={decrementCounter}>Decrement</button>
      <button onClick={() => {
        // Navigate to Home only after all state updates are processed
        Promise.resolve().then(() => {
          navigate('/');
        });
      }}>Go to Home</button>
    </div>
  );
};

